"""## 9. Compute joint ICs and flow matrices (pitch, duration, joint)"""

import itertools
import miditoolkit
import math
from copy import deepcopy

n = len(track_paths)

joint_H_pitch = np.full((n, n), np.nan)
joint_H_duration = np.full((n, n), np.nan)
joint_H_joint = np.full((n, n), np.nan)  # combined pitch+duration

# Make directory for merged MIDI files
os.makedirs("merged", exist_ok=True)

for i, j in itertools.combinations(range(n), 2):

    merged_path = f"merged/merged_{i}_{j}.mid"
    merge_two_tracks(track_paths[i], track_paths[j], merged_path)

    ic_all_xy, ic_pitch_xy, ic_duration_xy, note_mask_xy = compute_ic_components(merged_path)

    H_pitch_xy = float(np.mean(ic_pitch_xy)) if (ic_pitch_xy is not None and len(ic_pitch_xy) > 0) else float("nan")
    H_duration_xy = float(np.mean(ic_duration_xy)) if (ic_duration_xy is not None and len(ic_duration_xy) > 0) else float("nan")

    if (ic_pitch_xy is not None) and (ic_duration_xy is not None) and len(ic_pitch_xy) > 0 and len(ic_duration_xy) > 0:
        H_joint_xy = float(np.mean(ic_pitch_xy + ic_duration_xy))
    else:
        H_joint_xy = float("nan")

    joint_H_pitch[i, j] = joint_H_pitch[j, i] = H_pitch_xy
    joint_H_duration[i, j] = joint_H_duration[j, i] = H_duration_xy
    joint_H_joint[i, j] = joint_H_joint[j, i] = H_joint_xy

# Build flow matrices: Flow(i,j) = H(i) + H(j) - H_ij
Flow_pitch = np.zeros((n, n))
Flow_duration = np.zeros((n, n))
Flow_joint = np.zeros((n, n))

for i in range(n):
    for j in range(n):
        if i == j:
            Flow_pitch[i, j] = 0.0
            Flow_duration[i, j] = 0.0
            Flow_joint[i, j] = 0.0
            continue

        Hi_p, Hj_p = track_H_pitch[i], track_H_pitch[j]
        Hij_p = joint_H_pitch[i, j]

        Hi_d, Hj_d = track_H_duration[i], track_H_duration[j]
        Hij_d = joint_H_duration[i, j]

        Hi_j, Hj_j = Hi_p + Hi_d, Hj_p + Hj_d
        Hij_j = joint_H_joint[i, j]

        if not (math.isnan(Hi_p) or math.isnan(Hj_p) or math.isnan(Hij_p)):
            Flow_pitch[i, j] = Hi_p + Hj_p - Hij_p
        else:
            Flow_pitch[i, j] = float("nan")

        if not (math.isnan(Hi_d) or math.isnan(Hj_d) or math.isnan(Hij_d)):
            Flow_duration[i, j] = Hi_d + Hj_d - Hij_d
        else:
            Flow_duration[i, j] = float("nan")

        if not (math.isnan(Hi_j) or math.isnan(Hj_j) or math.isnan(Hij_j)):
            Flow_joint[i, j] = Hi_j + Hj_j - Hij_j
        else:
            Flow_joint[i, j] = float("nan")

