"""## 8. Helper: merge two tracks into a temporary MIDI"""

def merge_two_tracks(path_i, path_j, out_path):
    """Merge two single-instrument MIDI files into one multi-track MIDI and save to out_path."""
    mi = miditoolkit.MidiFile(path_i)
    mj = miditoolkit.MidiFile(path_j)

    m = miditoolkit.MidiFile()
    m.ticks_per_beat = mi.ticks_per_beat
    m.tempo_changes = mi.tempo_changes
    m.time_signature_changes = mi.time_signature_changes

    # Append instruments from both
    m.instruments = []
    for inst in mi.instruments:
        m.instruments.append(deepcopy(inst))
    for inst in mj.instruments:
        m.instruments.append(deepcopy(inst))

    m.dump(out_path)
