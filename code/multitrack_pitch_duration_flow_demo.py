    """Convert a MIDI file path to a (L, T) numpy int64 token array using SkyTNT's tokenizer."""
    with open(path, "rb") as f:
        data = f.read()
    midi_score = MIDI.midi2score(data)
    seq = tokenizer.tokenize(midi_score)
