"""## 5. Compute IC components (all, pitch-only, duration-only)"""

def compute_ic_components(path):
    """
    Compute IC components for a single-track MIDI at `path`.

    Returns:
      ic_all:      np.array, IC per event (mean over all tokens in the event)
      ic_pitch:    np.array or None, IC for the pitch token of note events
      ic_duration: np.array or None, IC for the duration token of note events
      note_mask:   boolean np.array over events (same length as ic_all) indicating which are 'note'
    """
    tokens = midi_path_to_tokens(path, tokenizer)   # shape: (L, T)
    if tokens.shape[0] < 2:
        return np.array([]), None, None, None

    # Prepare input/target
    ids = torch.tensor(tokens, dtype=torch.long, device=device).unsqueeze(0)  # (1, L, T)

    with torch.no_grad():
        x = ids[:, :-1]   # prefix events (1, L-1, T)
        y = ids[:, 1:]    # target events (1, L-1, T)

        hidden = model(x)               # (1, L-1, D, ...)
        hidden = hidden.reshape(-1, hidden.shape[-1])  # (N, D)

        y_flat = y.reshape(-1, y.shape[-1])            # (N, T)
        x_tok  = y_flat[:, :-1]                        # (N, T-1), within-event prefix tokens

        # Predict tokens at positions 1..T-1
        logits = model.forward_token(hidden, x_tok)    # (N, T-1, vocab)
        log_probs = F.log_softmax(logits, dim=-1)      # (N, T-1, vocab)

        # Targets are the next tokens in y_flat
        target = y_flat[:, 1:].unsqueeze(-1)           # (N, T-1, 1)
        token_log_probs = log_probs.gather(-1, target).squeeze(-1)  # (N, T-1)

        # Build a "full" log-prob array of length T, padding position 0 (unused)
        pad_col = torch.zeros(token_log_probs.size(0), 1, device=token_log_probs.device)
        token_log_probs_full = torch.cat([pad_col, token_log_probs], dim=1)  # (N, T)

        # 1) Event-level IC (mean over all tokens in event)
        ic_all = -token_log_probs_full.mean(dim=-1).cpu().numpy()  # (N,)

        # 2) Note-only IC for pitch & duration
        event_type_ids = y_flat[:, 0]               # type token at position 0
        note_mask_t = (event_type_ids == note_type_id)  # boolean mask
        note_mask = note_mask_t.cpu().numpy().astype(bool)

        if note_mask.any():
            note_log_probs = token_log_probs_full[note_mask]  # (N_notes, T)

            pitch_lp    = note_log_probs[:, note_pitch_pos]
            duration_lp = note_log_probs[:, note_duration_pos]

            ic_pitch    = (-pitch_lp).cpu().numpy()
            ic_duration = (-duration_lp).cpu().numpy()
        else:
            ic_pitch = None
            ic_duration = None

    return ic_all, ic_pitch, ic_duration, note_mask

